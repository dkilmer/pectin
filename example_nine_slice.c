void sprite_for_extra(float x, float y, int layer, int ridx, int cidx, int extra, sprite *s, render_def *rd) {
	s->x = (x + 0.5f + rd->xoff) + (rd->layer_xoff * (float)layer);
	s->y = (y + 0.5f + rd->yoff) + (rd->layer_yoff * (float)layer);
	s->z = ((((level_h-y)*level_w)+x) * -0.01f) + ((float)layer*-1.0f);
	s->r = 0.556863f;
	s->g = 0.443137f;
	s->b = 0.329412f;
	s->scale_x = rd->xscale * 0.5f;
	s->scale_y = rd->yscale * 0.5f;
	s->rot = 0.0f;
	s->spr_idx = ((ridx * rd->cols) + cidx);
	s->spr_cols = rd->cols;
	s->spr_extra = extra;
}

